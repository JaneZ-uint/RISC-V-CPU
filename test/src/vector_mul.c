// 重新生成的随机数据 (范围 10-99)
int multiply(int a, int b) {
    int result = 0;
    int neg = 0;
    
    if (a < 0) { a = -a; neg = !neg; }
    if (b < 0) { b = -b; neg = !neg; }
    
    while (b) {
        if (b & 1) result += a;
        a <<= 1;
        b >>= 1;
    }
    
    return neg ? -result : result;
}

